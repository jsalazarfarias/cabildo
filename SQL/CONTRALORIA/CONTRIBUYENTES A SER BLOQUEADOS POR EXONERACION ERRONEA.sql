select GEN01CODI, PK_UTI.GEN01_COM(GEN01CODI), 
       EMI01CLAVE
from emi01 where emi01titu LIKE ('%EXONERACION INSTITUCION DEL ESTADO. COOTAD Art.509%')
AND EMI01ANIO = 2020
--AND EMI01ESTA = 'R'
AND EMI01CLAVE IN (SELECT PUR01PRED FROM PUR01 WHERE PUR01TAVRE <= 10000 AND PUR01.PUR01PRED = EMI01.EMI01CLAVE)
GROUP BY GEN01CODI, EMI01CLAVE

